        Avec de la saronite et le sang des deux abominations, l'arme sacrée a pu être reforgé et devint la hache que Darion nomma Tranchant de l'ombre, l'enveloppe de l'arme qu'on appellera ensuite Deuillelombre.<br><p>Darion souhaitait décupler le pouvoir de l'arme mais savait que c'était une des étapes la plus traître qui soit. Renforcer la hache en absorbant les âmes des créatures présentes dans la Citadelle de la Couronne de glace était son projet. Mais pour avoir vu Arthas faucher la vie de nombreux innocents avec sa lame runique, il savait que de tels actions pourraient condmaner la vie ou même l'âme de celui qui s'en chargerait. Darion savait donc que cette arme serait créé dans l'unique but de purger le mal et n'en serait pas le porteur. Il s'adressa donc au héros qui l'avait auparavant aidé à récuperer le marteau sacré et celui ci, dont les intentions étaient pures et désintéressés, élimina les séides présent dans la citadelle. Les âmes furent absorbés et Tranchant de l'ombre devint plus forte.<br>
        Pour que la hache puisse atteindre sa puissance maximale, Darion décida de lui imprégner les trois énergies vitales du chevalier de la mort. Les énergies impies, du sang et de givre. Et quoi de mieux que lui insuffler directement le pouvoir des trois puissants lieutenants du Roi-liche: le professeur Putricide qui manie les forces impies à travers son alchimie, la Reine de Sang Lana'thel et la reine du givre Sindragosa. Si leurs attaques étaient dévastatrices, elles étaient aussi un excellent vecteur des ces énergies et c'est bien ce que Darion souhaitait exploiter. En les affrontant, ils voulaient en absorber une partie avec l'arme pour que celle-ci atteigne le paroxysme de ses pouvoirs. Et c'est ce que les héros d'Azeroth parvinrent à faire en les affrontant. Outre avoir absorber cette puissance colossale, les derniers remparts menant au Roi-Liche furent éliminés. Mais avant d'aller mener cet ultime combat, la puissance qui venait d'être insufflée à l'arme devait être scellé.
        Ainsi et en guise d'ultime composant, Darion fit rassembler des éclats ombregivres qui auraient ces propriétés. Et pour cause, ils sont les derniers vestiges de la prison qui enfermait l'esprit de Ner'Zhul, cette prison de glace créée par Kil'Jaeden à partir du Néant Distordu. Ces cristaux qui avaient retenus l'âme assoiffée de vengeance de Ner'Zhul pourraient parfaitement contenir les pouvoirs impies, de sang et de givre des trois généraux du Fléau au sein de la hache.<br>
        Darion utilisa ses pouvoirs runiques afin de sceller définitivement la puissance de l'arme et ainsi naquit la mythique Deuillelombre.<br>
        Il ne restait plus qu'elle accomplisse l'unique but pour lequel elle a été créée: venger les innombrables héros qui ont été victimes de cette sanglante campagne en éliminant le Roi-Liche.</p></span>
        
